        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> Login(LoginViewModel model, string returnUrl, Dictionary<string, object> CustomLoginDetails = null)
        {
            if (!ModelState.IsValid)
            {
                return View(model);
            }

            if (CustomLoginDetails != null)
            {
                if (CustomLoginDetails.ContainsKey("LoginDetails"))
                {
                    var customObject = CustomLoginDetails["LoginDetails"];
                    model =(LoginViewModel) customObject;
                } 
            }
            AuthenticationManager.SignOut(DefaultAuthenticationTypes.ApplicationCookie);
            // This doesn't count login failures towards account lockout
            // To enable password failures to trigger account lockout, change to shouldLockout: true
            var result = await SignInManager.PasswordSignInAsync(model.Email, model.Password, model.RememberMe, shouldLockout: false);
            switch (result)
            {
                case SignInStatus.Success:
                    HttpContext.Session.Clear();
                    var userId = SignInManager.AuthenticationManager.AuthenticationResponseGrant.Identity.GetUserId();
                    TempData["UserId"] = userId;
                    TempData["Email"] = model.Email;
                    TempData["Password"] = model.Password;
                    if (returnUrl != null)
                    {
                     return RedirectToLocal(returnUrl);
                    }
                   return RedirectionUrls(model.Email);
                case SignInStatus.LockedOut:
                    return View("Lockout");
                case SignInStatus.RequiresVerification:
                    return RedirectToAction("SendCode", new { RememberMe = model.RememberMe });
                case SignInStatus.Failure:
                default:
                    //this.AddNotification("Oops! username or password incorrect", NotificationType.ERROR);
                    ModelState.AddModelError("", "Oops! username or password incorrect");
                    return View(model);
            }
        }

